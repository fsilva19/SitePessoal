var clique_inicio = document.getElementById("inicio");


function clique() {
    clique_inicio.style.backgroundImage = "linear-gradient(to right, #FD5341 ,#7A0B00)";
    clique_inicio.style.color = "white";
}

function solte() {
    clique_inicio.style.backgroundImage = "linear-gradient(to right, #00c1ff, #228aab)";
    clique_inicio.style.color = "black";
}